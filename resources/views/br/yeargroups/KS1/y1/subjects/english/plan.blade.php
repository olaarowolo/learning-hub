            <h2>Overview</h2>
            <p>This page outlines the curriculum plan for Year 1 English, providing a structured overview of the topics and skills to be covered throughout the year. The focus is on developing foundational skills in phonics, reading, writing, and speaking.</p>
            <h3>Key Areas of Focus</h3>
            <ul>
                <li><strong>Phonics:</strong> Securing Phase 3-5 phonics knowledge for reading and spelling.</li>
                <li><strong>Reading:</strong> Building fluency and comprehension with simple texts and high-frequency words.</li>
                <li><strong>Writing:</strong> Forming letters correctly and writing simple sentences with correct punctuation.</li>
                <li><strong>Speaking & Listening:</strong> Participating in discussions and retelling stories.</li>
            </ul>
            <h3>Term Breakdown</h3>
            <ul>
                <li><strong>Autumn Term:</strong> Phonics, early reading, sentence writing, speaking and listening.</li>
                <li><strong>Spring Term:</strong> Developing fluency, comprehension, and sentence structure.</li>
                <li><strong>Summer Term:</strong> Consolidation, imaginative writing, punctuation, and grammar.</li>
            </ul>
