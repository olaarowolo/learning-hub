            <h2>Overview</h2>
            <p>This page outlines the curriculum plan for Year 0 English, providing a structured overview of the topics and skills to be covered throughout the year, focusing on the foundational areas of phonics, reading, and writing.</p>
            <h3>Key Areas of Focus</h3>
            <ul>
                <li><strong>Phonics:</strong> Learning letter sounds (phonemes) and how to blend them for reading and segment them for spelling.</li>
                <li><strong>Reading:</strong> Developing comprehension skills, reading common exception words, and enjoying a range of books.</li>
                <li><strong>Writing:</strong> Forming letters correctly, writing simple words and sentences, and expressing ideas.</li>
                <li><strong>Speaking and Listening:</strong> Participating in discussions, listening to stories, and expressing themselves clearly.</li>
            </ul>
            <h3>Term Breakdown</h3>
            <ul>
                <li><strong>Autumn Term:</strong> Introduction to Phonics (Phase 2), early mark-making, and sharing stories.</li>
                <li><strong>Spring Term:</strong> Continuing Phonics (Phase 3), blending and segmenting CVC words, and beginning to write simple captions.</li>
                <li><strong>Summer Term:</strong> Introduction to Phonics (Phase 4), reading and writing simple sentences, and retelling familiar stories.</li>
            </ul>
