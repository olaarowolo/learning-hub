    const skipCountingActivities = [
      {seq:[2,4,6,8,10], missing:[4,6,8]},  // by 2
      {seq:[5,10,15,20,25], missing:[10,15,20]}, // by 5
      {seq:[10,20,30,40,50], missing:[20,30,40]}, // by 10
      {seq:[12,14,16,18,20], missing:[14,16,18]}, // by 2
      {seq:[15,20,25,30,35], missing:[20,25,30]}, // by 5
      {seq:[30,40,50,60,70], missing:[40,50,60]}, // by 10
      {seq:[1,3,5,7,9], missing:[3,5,7]}, // by 2
    ];

    const container = document.getElementById('interactive-skip-counting');

    skipCountingActivities.forEach((act, idx) => {
      const div = document.createElement('div');
      div.className = 'interactive-box';
      div.innerHTML = `<strong>Activity ${idx+1}:</strong><br>Complete the sequence: ${act.seq.map((n,i)=> act.missing.includes(n)? `<input type="text" id="sc${idx}-${i}">`: n).join(', ')}
      <button onclick="checkSkip(${idx})">Check Answers</button>
      <p id="sc${idx}-feedback"></p>`;
      container.appendChild(div);
    });

    function checkSkip(idx){
      const act = skipCountingActivities[idx];
      let correct = true;
      act.missing.forEach((val) => {
        const input = document.getElementById(`sc${idx}-${act.seq.indexOf(val)}`);
        if(parseInt(input.value) !== val) correct = false;
      });
      const feedback = document.getElementById(`sc${idx}-feedback`);
      feedback.textContent = correct ? '✅ Correct!' : '❌ Try again!';
      feedback.style.color = correct ? 'green' : 'red';
    }
  </script>
